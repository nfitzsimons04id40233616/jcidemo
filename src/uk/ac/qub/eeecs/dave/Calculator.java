package uk.ac.qub.eeecs.dave;

import java.lang.Math;

public class Calculator {
	public int add(int a, int b) {
		return a+b;
	}
	
	public int sub(int a, int b) {
		return a-b;
	} 
	
	public int multiple(int a, int b) {
		return a*b;
	}
	
	public double divide(int a, int b) {
		return a/b;
	}
	
	
}
